function myMoveA() {
    var elem = document.getElementById("a");   
    elem.style.left="40vw";
}

function myOpacityA() {
    var arrayOfElements = document.getElementsByClassName("non-final a");
    var lengthOfArray=arrayOfElements.length;
    var moveButtonElement = document.getElementsByClassName("moveButton a");
    var lengthOfButtonArray = moveButtonElement.length;
    var originalButtonElement = document.getElementsByClassName("originalButton a");
    var lengthOfOriginalButtonArray = originalButtonElement.length;

    for (var i=0; i<lengthOfArray;i++){
        arrayOfElements[i].style.opacity="100";
    }

    for (var i = 0; i < lengthOfButtonArray; i++) {
        moveButtonElement[i].style.display = "none";
    }

    for (var i = 0; i < lengthOfOriginalButtonArray; i++) {
        originalButtonElement[i].style.display = "inline";
    }

    console.log(arrayOfElements);
}

